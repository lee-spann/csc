
locations = ['london', 'rome', 'japan', 'paris', 'jamaica']


print (locations)

#alphabetical order
print (sorted(locations))

print (locations)

#reverse alphabetical order
print(sorted(locations, reverse=True))

print (locations)

#reverse order
locations.reverse()

print (locations)

#sort
locations.sort()

print (locations)

#sort reverse
locations.sort(reverse = True)

print(locations)